# 2 2 list in python
# This list will store 2 elements

list_22 = [5, 10]

# printing the list
print("Initial List:", list_22)

# changing an element in the list
list_22[0] = 7
print("After Changing List:", list_22)

# adding an element at the end of the list
list_22.append(15)
print("After Adding Element at the End:", list_22)

# inserting an element at a specified position
list_22.insert(1, 20)
print("After Inserting Element at a Position:", list_22)
